type SVGIcon struct {
	app.Compo
	IconData       IconData
	IconSize       IconSize
	Color          string
	OpacityPercent int
}

type IconSize int

const (
	IconSizeTiny   IconSize = 2
	IconSizeSmall  IconSize = 3
	IconSizeMedium IconSize = 4
	IconSizeBig    IconSize = 5
	IconSizeLarge  IconSize = 6
	IconSizeHuge   IconSize = 12
)

func (s *SVGIcon) Render() app.UI {
	if s.IconSize == 0 {
		s.IconSize = IconSizeMedium
	}
	if s.Color == "" {
		s.Color = "white"
	}

	if s.OpacityPercent == 0 {
		s.OpacityPercent = 100
	}

	if s.OpacityPercent < 0 {
		s.OpacityPercent = 0
	}

	svgContent := string(s.IconData)

	if !strings.Contains(svgContent, `fill="`) {
		svgContent = strings.Replace(svgContent, "<svg", fmt.Sprintf(`<svg fill="%s"`, s.Color), 1)
	} else {
		svgContent = strings.Replace(svgContent, `fill="currentColor"`, fmt.Sprintf(`fill="%s"`, s.Color), -1)
	}

	return app.Div().
		Class(fmt.Sprintf("w-%d h-%d flex justify-center align-center items-center opacity-%d", s.IconSize, s.IconSize, s.OpacityPercent)).
		Body(app.Raw(svgContent))
}
